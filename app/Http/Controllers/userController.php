    {
        $usuario = new User();   
            $usuario->area = $request->input('area');
            $usuario->tipo_usuario = $request->input('tipo_usuario');
            $usuario->nom_usuario = $request->input('nom_usuario');
            $usuario->password = bcrypt($request->input('password'));
            $usuario->save();
    }

